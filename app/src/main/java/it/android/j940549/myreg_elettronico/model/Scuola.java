package it.android.j940549.myreg_elettronico.model;

public class Scuola {

    String nomescuola,tipo;
    String indirizzo,comune;
    String cfscuola;

    public Scuola() {
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getComune() {
        return comune;
    }

    public void setComune(String comune) {
        this.comune = comune;
    }

    public String getNomescuola() {
        return nomescuola;
    }

    public void setNomescuola(String nomescuola) {
        this.nomescuola = nomescuola;
    }

    public String getIndirizzo() {
        return indirizzo;
    }

    public void setIndirizzo(String indirizzo) {
        this.indirizzo = indirizzo;
    }

    public String getCfscuola() {
        return cfscuola;
    }

    public void setCfscuola(String cfscuola) {
        this.cfscuola = cfscuola;
    }


}
